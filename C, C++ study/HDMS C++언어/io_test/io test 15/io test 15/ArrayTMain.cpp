#include <iostream>
using namespace std;
#include "SmartAryT.h"
int main()
{
	cout.setf(ios::fixed, ios::floatfield);
	cout.precision(2);
	SmartArray<double> height(3);

	int i;
	bool res;
	double tot = 0;
	double temp;

	cout << "세 학생의 키 입력 하기... " << endl;
	for (i = 0; i < 3; i++)
	{
		cout << i + 1 << " 번 학생의 키 입력 : ";
		cin >> temp;
		height.put(i, temp);
	}
	for (i = 0; i < 3; i++)
	{
		res = height.get(i, temp);
		if (res)
			tot += temp;
		else
			cout << "배열 읽기 실패" << endl;
	}
	cout << "평균 키 : " << tot / 3.0 << endl;
	return 0;
}
